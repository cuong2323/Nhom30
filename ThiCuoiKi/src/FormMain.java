    private void loadTableData() {
        tableModel.setRowCount(0); // Xóa tất cả dữ liệu trong bảng
        List<NhanVien> list = nvManager.getAllNhanVien();
        for (NhanVien nv : list) {
            Object[] row = {
                nv.getMaNhanVien(),
                nv.getTenNhanVien(),
                nv.getBoPhan(),
                nv.getChucVu(),
                nv.getGioiTinh(),
                nv.getEmail(),
                nv.getSoDienThoai()
            };
            tableModel.addRow(row);
        }
    }

