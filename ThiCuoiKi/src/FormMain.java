    private void btnTimkiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimkiemActionPerformed
        String searchQuery = txtManvTimKiem.getText();

        // Kiểm tra nếu người dùng nhập vào là một số nguyên (mã nhân viên)
        try {
            int searchMaNhanVien = Integer.parseInt(searchQuery);  // Nếu có thể chuyển đổi sang int
            List<NhanVien> searchResults = nvManager.searchNhanVien(searchMaNhanVien);  // Tìm kiếm theo mã nhân viên

            // Xóa tất cả dữ liệu trong bảng
            tableModel.setRowCount(0);

            // Thêm các dòng tìm thấy vào bảng
            for (NhanVien nv : searchResults) {
                Object[] row = {
                    nv.getMaNhanVien(),
                    nv.getTenNhanVien(),
                    nv.getBoPhan(),
                    nv.getChucVu(),
                    nv.getGioiTinh(),
                    nv.getEmail(),
                    nv.getSoDienThoai()
                };
                tableModel.addRow(row);
            }
        } catch (NumberFormatException e) {
            // Nếu không thể chuyển sang int, tìm kiếm theo tên hoặc bộ phận
            List<NhanVien> searchResults = nvManager.searchNhanVien(searchQuery);  // Tìm kiếm theo tên hoặc bộ phận

            // Xóa tất cả dữ liệu trong bảng
            tableModel.setRowCount(0);

            // Thêm các dòng tìm thấy vào bảng
            for (NhanVien nv : searchResults) {
                Object[] row = {
                    nv.getMaNhanVien(),
                    nv.getTenNhanVien(),
                    nv.getBoPhan(),
                    nv.getChucVu(),
                    nv.getGioiTinh(),
                    nv.getEmail(),
                    nv.getSoDienThoai()
                };
                tableModel.addRow(row);
            }
        }
    }//GEN-LAST:event_btnTimkiemActionPerformed

