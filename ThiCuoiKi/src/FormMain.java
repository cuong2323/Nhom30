    private void tblNhanVienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblNhanVienMouseClicked
        // TODO add your handling code here:
        // Kiểm tra nếu có dòng được chọn trong bảng
        int selectedRow = tblNhanVien.getSelectedRow();

        // Nếu chọn một dòng, thực hiện lấy dữ liệu từ dòng đó
        if (selectedRow != -1) {
            // Lấy dữ liệu từ các cột trong dòng được chọn
            int maNhanVien = (int) tblNhanVien.getValueAt(selectedRow, 0);  // Cột 0: Mã nhân viên
            String tenNhanVien = (String) tblNhanVien.getValueAt(selectedRow, 1);  // Cột 1: Tên nhân viên
            String boPhan = (String) tblNhanVien.getValueAt(selectedRow, 2);  // Cột 2: Bộ phận
            String chucVu = (String) tblNhanVien.getValueAt(selectedRow, 3);  // Cột 3: Chức vụ
            String gioiTinh = (String) tblNhanVien.getValueAt(selectedRow, 4);  // Cột 4: Giới tính
            String email = (String) tblNhanVien.getValueAt(selectedRow, 5);  // Cột 5: Email
            String soDienThoai = (String) tblNhanVien.getValueAt(selectedRow, 6);  // Cột 6: Số điện thoại

            // Cập nhật các JTextField và JComboBox với giá trị từ dòng được chọn
            txtManv.setText(String.valueOf(maNhanVien));  // Chuyển đổi int sang String
            txtTennv.setText(tenNhanVien);
            txtBophan.setText(boPhan);
            txtChucVu.setText(chucVu);

            // Giới tính có thể là "Nam" hoặc "Nữ", hãy chọn đúng giá trị trong JComboBox
            jcomboxGioiTinh.setSelectedItem(gioiTinh);

            txtEmail.setText(email);
            txtSodienthoai.setText(soDienThoai);
        }
    }//GEN-LAST:event_tblNhanVienMouseClicked

