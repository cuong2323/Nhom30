    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        try {
            // Lấy giá trị từ các trường nhập liệu
            int maNhanVien = Integer.parseInt(txtManv.getText());  // Chuyển đổi từ String sang int
            String tenNhanVien = txtTennv.getText();
            String boPhan = txtBophan.getText();
            String chucVu = txtChucVu.getText();
            String gioiTinh = jcomboxGioiTinh.getSelectedItem().toString();
            String email = txtEmail.getText();
            String soDienThoai = txtSodienthoai.getText();

            // Gọi phương thức addNhanVien để thêm nhân viên vào cơ sở dữ liệu
            boolean isAdded = nvManager.addNhanVien(maNhanVien, tenNhanVien, boPhan, chucVu, gioiTinh, email, soDienThoai);

            if (isAdded) {
                JOptionPane.showMessageDialog(this, "Nhân viên đã được thêm thành công!");
                loadTableData();  // Tải lại dữ liệu bảng
            } else {
                JOptionPane.showMessageDialog(this, "Thêm nhân viên không thành công!");
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Mã nhân viên phải là một số nguyên hợp lệ!");
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        int selectedRow = tblNhanVien.getSelectedRow();
        if (selectedRow != -1) {
            try {
                int maNhanVien = (int) tblNhanVien.getValueAt(selectedRow, 0);  // Lấy MaNhanVien từ bảng
                String tenNhanVien = txtTennv.getText();
                String boPhan = txtBophan.getText();
                String chucVu = txtChucVu.getText();
                String gioiTinh = jcomboxGioiTinh.getSelectedItem().toString();
                String email = txtEmail.getText();
                String soDienThoai = txtSodienthoai.getText();

                boolean isUpdated = nvManager.updateNhanVien(maNhanVien, tenNhanVien, boPhan, chucVu, gioiTinh, email, soDienThoai);
                if (isUpdated) {
                    JOptionPane.showMessageDialog(this, "Cập nhật nhân viên thành công!");
                    loadTableData();
                } else {
                    JOptionPane.showMessageDialog(this, "Cập nhật nhân viên thất bại!");
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Lỗi khi cập nhật dữ liệu! Vui lòng thử lại.");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn nhân viên cần sửa!");
        }
    }//GEN-LAST:event_btnSuaActionPerformed

